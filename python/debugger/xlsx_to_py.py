from time import gmtime, strftime
import pandas
import pprint
import numpy


def pd_to_dict(df):
    # filter out invalid recorder
    valid = ~df.iloc[:, 1].isnull()
    df = df[valid].copy()
    return list(zip(df.iloc[:, 0], numpy.cumsum(df.iloc[:, 1].astype(int))))


