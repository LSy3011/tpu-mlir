reg_bdc = pandas.read_excel(tiu_reg_bm1684x, sheet_name=bdc_sheet_name)
reg_dma = pandas.read_excel(dma_reg_bm1684x, sheet_name=dma_sheet_name)

cmd_reg = {}
for k, df in reg_bdc.items():
    cmd_reg[k] = pd_to_dict(df)

